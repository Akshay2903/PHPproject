    <h2>What is heat exhaustion?</h2>
    <p>Heat exhaustion is a condition that occurs due to overheating of the body. Heat exhaustion can occur because of
	
	<ul>1.Water depletion: Symptoms include excessive thirst, weakness, headache, loss of consciousness etc.</ul>
	<ul>2.Salt depletion: Symptoms include nausea, vomiting, muscle cramps, dizziness etc.</ul>
	</p>
	<h2>Symptoms of heat exhaustion:</h2>
	<p>
	<b>Cool, moist skin with goose bumps when in the heat</b></br>
    <b>  Heavy sweating</b></br>
    <b>Faintness</b></br>
    <b>Dizziness</b></br>
    <b><a href="https://en.wikipedia.org/wiki/Fatigue_(medical)" target="_blank">Fatigue</b></a></br>
    <b>Rapid heartbeat</b></br>
    <b>Low blood pressure upon standing</b></br>
    <b>Muscle or abdominal cramps</b></br>
    <b>Nausea</b></br>
    <b>Headache</b></br>
    <b>Confusion</b></br>
	</p>
	<p>Although, heat exhaustion isn’t as severe or life threatening as a heatstroke, it isn’t something that should be taken lightly. If left unattended, heat exhaustion can lead to heatstroke.</p>
  
     
    <h2>What is a heat stroke?</h2>
       <p>Prolonged exposure to or physically strenuous activity in elevated temperatures can lead to heatstroke. This condition is very severe,</br> and if left untreated can quickly damage your brain, heart, kidneys and muscles. This is the most serious form of heat injury and should be treated as an emergency.</p>
   
    
    <h2>Symptoms of heat stroke:</h2>
      <p>
	  <b>High body temperature: This is the most important symptom. A core body temperature of 40° C or higher, is the main sign of heatstroke.</b></br>
	  <b>Confusion and agitation</b></br>
	  <b><a href="https://en.wikipedia.org/wiki/Delirium" target="_blank">Slurred speech and delirium</b></a></br>
	  <b><a href="https://en.wikipedia.org/wiki/Fatigue_(medical)" target="_blank">Seizures</b></a></br>
	  <b>A comatose state</b></br>
	  <b>If the heatstroke is brought on by hot weather, your skin may feel hot and dry to the touch. Heatstroke brought on by strenuous exercise may leave your skin feeling dry and slightly moist.</b></br>
	  <b>Nausea and vomiting</b></br>
	  <b>Flushed skin</b></br>
	  <b>Headache</b></br>
	  <b>Racing heartbeat</b></br>
	  <b>Rapid breathing</b>
	  </p>
    
     
    <h3>Heat exhaustion v/s Heatstroke</h3>
	<p>People often get confused between heat exhaustion and heatstroke, which sometimes leads to delay in getting proper medical help.</br> This can be dangerous in cases of heatstroke as the damage it causes to the body increases with the time it is left untreated. Here are a few simple ways in which you can differentiate between the two:
</p>
     <p><b>Check your body temperature. If it is above 40° C and continues to remain so, you may be suffering from a heatstroke.</b></br>
<b>Generally, while experiencing heat exhaustion a person tends to sweat excessively, whereas in a heatstroke they stop sweating and are actually dry.</b>
</br><b>Symptoms experienced during a heatstroke are more severe as compared to symptoms experienced during heat exhaustion.</b></p>
	 <p>If you notice any symptoms of heat exhaustion or heatstroke it is advisable to retreat to a cool room, loosen tight clothing, mist your body with water and rehydrate.</br> If you suspect a heatstroke, <a href="http://localhost/myproject/Appointment.php" target="_blank">consult your doctor</a> immediately. It is always better to be safe than sorry.</p>
