					   <h1>Heatstroke Vs. Heat Exhaustion</h1>
					   <p>With summer approaching, it is important for us to be aware of and understand heat related illnesses, their symptoms and how to avoid them. Heat related illnesses include heat cramps (mild form),</br> heat exhaustion (slightly severe) and heatstroke (very severe). Performing strenuous physical activity in a hot environment can hinder the body’s ability to cool itself and regulate temperature.</br> This overheating of the body coupled with dehydration can lead to the onset of heat related illnesses.

</p>
